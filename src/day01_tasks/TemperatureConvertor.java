package day01_tasks;

public class TemperatureConvertor {
    public static void main(String[] args) {
        // write Java program to convert F to C
        // input F 212
        // formulla C= (5(F-32))/9
        int fahr = 212;
        double celcius = (5 *(fahr-32))/9;
        System.out.println("celcius = " + celcius); //celcius.soutv
    }
}
